void colorit(int col)
	{                  
		HANDLE hstdout;
		hstdout = GetStdHandle( STD_OUTPUT_HANDLE );	         // 12=red, 15=white --> this.colorit(12) ; this.colorit(15);
        SetConsoleTextAttribute( hstdout, col );
    }
